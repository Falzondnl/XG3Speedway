def _persist(match_id: str, report: Dict) -> bool:
    try:
        db_url = os.getenv("DATABASE_URL")
        if not db_url:
            logger.warning("DATABASE_URL not set — settlement not persisted to DB")
            return False
        import psycopg2
        conn = psycopg2.connect(db_url)
        cur = conn.cursor()
        cur.execute("""
            CREATE TABLE IF NOT EXISTS speedway_settlement_records (
                match_id VARCHAR PRIMARY KEY,
                markets_graded INT,
                report_json TEXT,
                settled_at TIMESTAMPTZ DEFAULT NOW()
            )
        """)
        cur.execute("""
            INSERT INTO speedway_settlement_records (match_id, markets_graded, report_json)
            VALUES (%s, %s, %s)
            ON CONFLICT (match_id) DO UPDATE
            SET markets_graded = EXCLUDED.markets_graded,
                report_json    = EXCLUDED.report_json,
                settled_at     = NOW()
        """, (match_id, report.get("markets_graded", 0), json.dumps(report)))
        conn.commit()
        cur.close()
        conn.close()
        return True
    except Exception as e:
        logger.error(f"DB persist failed for {match_id}: {e}")
        return False


def _load_from_db(match_id: str) -> Optional[Dict]:
    try:
        db_url = os.getenv("DATABASE_URL")
        if not db_url:
            return None
        import psycopg2
        conn = psycopg2.connect(db_url)
        cur = conn.cursor()
        cur.execute(
            "SELECT report_json FROM speedway_settlement_records WHERE match_id = %s",
            (match_id,)
        )
        row = cur.fetchone()
        cur.close()
        conn.close()
        return json.loads(row[0]) if row else None
    except Exception:
        return None


