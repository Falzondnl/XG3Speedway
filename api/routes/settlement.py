@router.post("/grade/{match_id}", response_model=SettlementReport)
async def grade_match(match_id: str, result: MatchResult) -> SettlementReport:
    """
    Auto-grade all markets for a completed Speedway match.
    Automatically persists to PostgreSQL.
    """
    try:
        from api.routes.markets import get_markets_for_match
        markets = await get_markets_for_match(match_id)
    except (ImportError, Exception):
        markets = []

    report = _engine.grade_match(match_id, result, markets)
    persisted = _persist(match_id, report)
    _cache[match_id] = report

    logger.info(
        f"[settlement][speedway] {match_id}: {report['markets_graded']} markets, DB={persisted}"
    )
    return SettlementReport(
        match_id=match_id,
        markets_graded=report["markets_graded"],
        bets_settled=report["markets_graded"],
        status="settled",
        timestamp=datetime.now(timezone.utc).isoformat(),
        persisted_to_db=persisted,
    )


@router.get("/status/{match_id}")
async def settlement_status(match_id: str) -> Dict:
    if match_id in _cache:
        return _cache[match_id]
    db_report = _load_from_db(match_id)
    if db_report:
        _cache[match_id] = db_report
        return db_report
    raise HTTPException(status_code=404, detail=f"No settlement for {match_id}")


